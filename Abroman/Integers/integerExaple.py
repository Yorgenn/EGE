# в питоне 3 вида деления
# 1 / арефметик(универсальное)работает для любых двух чисел, РЕЗУЛЬТАТОМ ВСЕГДА БУДЕТ FLOAT
print(6 / 3)
# следующие два вида работают только для int
# 2ой тип // получение целой части от деления(цклочисленное деление)
print(17 // 5)
# 3ий % получение остатка от деления
print(17 % 5)
print(20 % 2)
