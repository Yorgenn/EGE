y1: float = float(input("Введите первое число y:"))
y2: float = float(input("Введите второе число y:"))

lenthg: float = math.sqrt((x2-x1)**2+(y2-y1)**2)

print(f"Расстояние между двумя точками равно: {lenthg}")    #TODO нужна помощь ниче не понял