# операции сравнения в питоне- всегда возвращают bool
# 1)== - проверка на равенство
# 2)!= - проверка на не равенство

print(5 == 5)
print(5 == 4)
print(5 != 5)
print(5 != 4)

# 3) > - больше(проверка что обьект слева больше чем справа)
# 4)>= - больше или равно
# 5)<= - меньше или равно
# #6)<меньше

print(0>-2)
print(5>=5)
